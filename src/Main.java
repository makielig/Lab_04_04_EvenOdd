public class Main {
    public static void main(String[] args) {
        int numToExamine = 2; // Example number to examine

        // Determine if the number is even or odd using the modulo operator
        if (numToExamine % 2 == 0) {
            System.out.println(numToExamine + " is Even.");
        } else {
            System.out.println(numToExamine + " is Odd.");
        }
    }
}